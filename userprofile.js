const mongoose = require('mongoose');

let userProfileSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true
  },
  lastName: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  }
});

exports.UserProfile = mongoose.model('UserProfile', userProfileSchema);